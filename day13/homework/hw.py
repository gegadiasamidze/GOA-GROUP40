#Bold-ით დაწერე შენი სახელი.

#Italic-ით დაწერე შენი საყვარელი ცხოველის სახელი.

#Bold და italic-ით დაწერე შენი საყვარელი საჭმლის სახელი.

#Bold-ით დაწერე სამი ფერის სია, რომლებიც მოგწონს.

#Italic-ით დაწერე შენი საყვარელი წიგნის სათაური.

#Bold-ით გამოყავი სიტყვა „მნიშვნელოვანი“ წინადადებაში.

#Italic-ით დაწერე საინტერესო ფაქტი შენს შესახებ.

#Bold-ით დაწერე კვირის დღეები.

#Italic-ით დაწერე წლის თვეები.

